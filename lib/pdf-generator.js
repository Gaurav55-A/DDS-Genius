import PDFDocument from 'pdfkit';

/**
 * Generate DDR PDF Report matching Main DDR.pdf structure
 * This creates a professional Detailed Diagnosis Report with merged visual and thermal data
 */
export async function generateDDRPDF(reportData) {
  return new Promise((resolve, reject) => {
    try {
      const doc = new PDFDocument({ size: 'A4', margin: 50 });
      const chunks = [];

      doc.on('data', (chunk) => chunks.push(chunk));
      doc.on('end', () => resolve(Buffer.concat(chunks)));
      doc.on('error', reject);

      const { propertyInfo, mergedData, analytics } = reportData;

      // Page 1: Cover Page
      generateCoverPage(doc, propertyInfo, reportData.reportId);
      doc.addPage();

      // Page 2: Property Issue Summary
      generateSummaryPage(doc, mergedData);
      doc.addPage();

      // Page 3-N: Area-wise Observations
      generateAreaWiseObservations(doc, mergedData);
      doc.addPage();

      // Analysis & Recommendations
      generateAnalysisPage(doc, mergedData);
      doc.addPage();

      // Final Page: Limitations
      generateLimitationsPage(doc);

      doc.end();
    } catch (error) {
      reject(error);
    }
  });
}

function generateCoverPage(doc, propertyInfo, reportId) {
  // Header with electric yellow accent
  doc.rect(0, 0, 595, 80).fill('#FACC15');
  
  doc.fillColor('#0F172A')
     .fontSize(24)
     .font('Helvetica-Bold')
     .text('DETAILED DIAGNOSIS REPORT', 50, 25, { align: 'center' });

  doc.fillColor('#334155')
     .fontSize(12)
     .font('Helvetica')
     .text('Property Inspection & Thermal Analysis', 50, 55, { align: 'center' });

  // Property Details
  doc.fillColor('#0F172A')
     .fontSize(16)
     .font('Helvetica-Bold')
     .text('Property Information', 50, 150);

  const details = [
    ['Property Type:', propertyInfo?.type || 'Residential'],
    ['Inspection Date:', new Date(propertyInfo?.inspectionDate || Date.now()).toLocaleDateString()],
    ['Inspector:', propertyInfo?.inspector || 'DDR Genius AI'],
    ['Report ID:', reportId?.substring(0, 8) || 'N/A']
  ];

  let y = 180;
  details.forEach(([label, value]) => {
    doc.fillColor('#64748B')
       .fontSize(11)
       .font('Helvetica')
       .text(label, 50, y);
    
    doc.fillColor('#0F172A')
       .fontSize(11)
       .font('Helvetica-Bold')
       .text(value, 200, y);
    y += 25;
  });

  // Footer
  doc.fillColor('#94A3B8')
     .fontSize(9)
     .font('Helvetica')
     .text('Generated by DDR Genius | Made by Gaurav Agrawal', 50, 750, { align: 'center' });
}

function generateSummaryPage(doc, mergedData) {
  doc.fillColor('#0F172A')
     .fontSize(18)
     .font('Helvetica-Bold')
     .text('Property Issue Summary', 50, 50);

  doc.fillColor('#334155')
     .fontSize(11)
     .font('Helvetica')
     .text(mergedData?.propertyIssueSummary || 'Comprehensive inspection completed with thermal imaging analysis.', 50, 85, {
       width: 495,
       align: 'justify'
     });

  // Severity Assessment
  doc.fillColor('#0F172A')
     .fontSize(14)
     .font('Helvetica-Bold')
     .text('Severity Assessment', 50, 150);

  const severity = mergedData?.severityAssessment || { overall: 'Moderate', reasoning: 'Not Available' };
  
  const severityColor = {
    'Minor': '#2DD4BF',
    'Moderate': '#F59E0B',
    'Severe': '#FB7185'
  }[severity.overall] || '#F59E0B';

  doc.rect(50, 180, 100, 30).fill(severityColor);
  doc.fillColor('#0F172A')
     .fontSize(12)
     .font('Helvetica-Bold')
     .text(severity.overall, 55, 190);

  doc.fillColor('#334155')
     .fontSize(10)
     .font('Helvetica')
     .text(severity.reasoning || 'Based on visual and thermal inspection', 50, 225, {
       width: 495
     });

  // Probable Root Cause
  doc.fillColor('#0F172A')
     .fontSize(14)
     .font('Helvetica-Bold')
     .text('Probable Root Cause', 50, 280);

  doc.fillColor('#334155')
     .fontSize(10)
     .font('Helvetica')
     .text(mergedData?.probableRootCause || 'Further investigation required', 50, 310, {
       width: 495,
       align: 'justify'
     });

  // Recommended Actions
  doc.fillColor('#0F172A')
     .fontSize(14)
     .font('Helvetica-Bold')
     .text('Recommended Actions', 50, 380);

  let y = 410;
  const actions = mergedData?.recommendedActions || ['Conduct detailed inspection', 'Monitor affected areas'];
  actions.forEach((action, index) => {
    doc.fillColor('#FACC15').circle(60, y + 5, 3).fill();
    doc.fillColor('#334155')
       .fontSize(10)
       .font('Helvetica')
       .text(action, 75, y, { width: 470 });
    y += 25;
  });
}

function generateAreaWiseObservations(doc, mergedData) {
  doc.fillColor('#0F172A')
     .fontSize(18)
     .font('Helvetica-Bold')
     .text('Area-wise Observations', 50, 50);

  const observations = mergedData?.areaWiseObservations || [];
  let y = 90;

  observations.forEach((obs, index) => {
    if (y > 700) {
      doc.addPage();
      y = 50;
    }

    // Area header with electric blue accent
    doc.rect(50, y, 495, 25).fill('#3B82F6');
    doc.fillColor('#FFFFFF')
       .fontSize(12)
       .font('Helvetica-Bold')
       .text(`${obs.area || 'Unknown Area'}`, 60, y + 7);

    y += 35;

    // Visual Observation
    doc.fillColor('#0F172A')
       .fontSize(10)
       .font('Helvetica-Bold')
       .text('Visual Observation:', 50, y);
    
    doc.fillColor('#334155')
       .fontSize(9)
       .font('Helvetica')
       .text(obs.visualObservation || 'Not Available', 50, y + 15, { width: 495 });

    y += 40;

    // Thermal Reading
    doc.fillColor('#0F172A')
       .fontSize(10)
       .font('Helvetica-Bold')
       .text('Thermal Analysis:', 50, y);

    const thermal = obs.thermalReading || {};
    const thermalText = thermal.analysis || 
      `Hotspot: ${thermal.hotspot || 'N/A'}°C | Coldspot: ${thermal.coldspot || 'N/A'}°C`;
    
    doc.fillColor('#334155')
       .fontSize(9)
       .font('Helvetica')
       .text(thermalText, 50, y + 15, { width: 495 });

    y += 40;

    // Defect Type & Severity
    doc.fillColor('#64748B')
       .fontSize(9)
       .font('Helvetica')
       .text(`Type: ${obs.defectType || 'N/A'} | Severity: ${obs.severity || 'N/A'}`, 50, y);

    y += 30;

    // Divider
    doc.moveTo(50, y).lineTo(545, y).stroke('#E2E8F0');
    y += 15;
  });
}

function generateAnalysisPage(doc, mergedData) {
  doc.fillColor('#0F172A')
     .fontSize(18)
     .font('Helvetica-Bold')
     .text('Analysis & Additional Notes', 50, 50);

  // Additional Notes
  const notes = mergedData?.additionalNotes || 'All findings documented above.';
  doc.fillColor('#334155')
     .fontSize(10)
     .font('Helvetica')
     .text(notes, 50, 90, { width: 495, align: 'justify' });

  // Missing Information
  doc.fillColor('#0F172A')
     .fontSize(14)
     .font('Helvetica-Bold')
     .text('Missing or Unclear Information', 50, 180);

  const missingInfo = mergedData?.missingOrUnclearInfo || [];
  if (missingInfo.length > 0) {
    let y = 210;
    missingInfo.forEach(item => {
      doc.fillColor('#F59E0B').circle(60, y + 5, 3).fill();
      doc.fillColor('#334155')
         .fontSize(9)
         .font('Helvetica')
         .text(item, 75, y, { width: 470 });
      y += 20;
    });
  } else {
    doc.fillColor('#2DD4BF')
       .fontSize(10)
       .font('Helvetica-Bold')
       .text('All required data available', 50, 210);
  }

  // Data Conflicts
  const conflicts = mergedData?.dataConflicts || [];
  if (conflicts.length > 0) {
    doc.fillColor('#0F172A')
       .fontSize(14)
       .font('Helvetica-Bold')
       .text('Data Conflicts', 50, 280);

    doc.rect(50, 310, 495, 2).fill('#FB7185');

    let y = 320;
    conflicts.forEach(conflict => {
      doc.fillColor('#FB7185').circle(60, y + 5, 3).fill();
      doc.fillColor('#334155')
         .fontSize(9)
         .font('Helvetica')
         .text(conflict, 75, y, { width: 470 });
      y += 25;
    });
  }
}

function generateLimitationsPage(doc) {
  doc.fillColor('#0F172A')
     .fontSize(18)
     .font('Helvetica-Bold')
     .text('Limitations & Disclaimer', 50, 50);

  const limitations = [
    'This report is based on visual inspection and thermal imaging analysis.',
    'Findings are limited to accessible areas at the time of inspection.',
    'Some defects may not be visible or detectable through thermal imaging.',
    'This report does not constitute a structural engineering assessment.',
    'Further investigation may be required for areas marked with conflicts.',
    'Recommendations should be implemented by qualified professionals.'
  ];

  let y = 90;
  limitations.forEach((limitation, index) => {
    doc.fillColor('#FACC15').circle(60, y + 5, 3).fill();
    doc.fillColor('#334155')
       .fontSize(10)
       .font('Helvetica')
       .text(limitation, 75, y, { width: 470 });
    y += 30;
  });

  // Final note
  doc.rect(50, y + 20, 495, 80).fill('#F1F5F9');
  doc.fillColor('#0F172A')
     .fontSize(10)
     .font('Helvetica-Bold')
     .text('Report Generated By', 60, y + 35);
  
  doc.fillColor('#334155')
     .fontSize(9)
     .font('Helvetica')
     .text('DDR Genius - AI-Powered Diagnostic Reports', 60, y + 55)
     .text('Made by Gaurav Agrawal', 60, y + 70);
}
